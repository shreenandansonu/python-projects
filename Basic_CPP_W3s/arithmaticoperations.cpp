#include <iostream>
using namespace std;
int main()
{
    int c=5, b=10, d=15, e=66;
    cout<<c+b<<"\n";
    cout<<c-b<<"\n";
    cout<<c*b<<"\n";
    cout<<float(e/d)<<"\n";
    cout<<e%d<<"\n";
    cout<<e++<<"\n";
    cout<<e<<"\n";
    cout<<++e<<"\n";
    cout<<d--<<"\n";
    cout<<d<<"\n";
    cout<<--d<<"\n";
}